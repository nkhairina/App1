package quiz.quiz3;

public class question30 {
    public static void main(String[] args) {
        /*
         * This code demonstrates the use of variables of different data types and
         * arithmetic operations in Java.
         */

        // Integer variables
        int num1 = 10;
        int num2 = 20;

        // Double variable
        double decimalNum = 3.14;

        // Boolean variable
        boolean isTrue = true;

        // Character variable
        char initial = 'A';

        // String variable
        String name = "Alice";

        // Arithmetic operations
        int sum = num1 + num2;
        int difference = num2 - num1;
        double product = decimalNum * num1;
        double quotient = decimalNum / num2;
        int remainder = num2 % num1;

        // Print results
        System.out.println("Integer variables:");
        System.out.println("num1 = " + num1);
        System.out.println("num2 = " + num2);
        System.out.println("sum = " + sum);
        System.out.println("difference = " + difference);

        System.out.println("\nDouble variable:");
        System.out.println("decimalNum = " + decimalNum);
        System.out.println("product = " + product);
        System.out.println("quotient = " + quotient);

        System.out.println("\nBoolean variable:");
        System.out.println("isTrue = " + isTrue);

        System.out.println("\nCharacter variable:");
        System.out.println("initial = " + initial);

        System.out.println("\nString variable:");
        System.out.println("name = " + name);

        System.out.println("\nRemainder:");
        System.out.println("remainder = " + remainder);
    }
}
